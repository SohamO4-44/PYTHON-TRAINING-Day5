def add():
    n1 = int(input("Enter the value of n1: "))
    n2 = int(input("Enter the value of n2: "))

    addition = n1 + n2
    multiplication = n1 * n2
    subtraction = n1 - n2
    division = n1 / n2

    print("Addition =", addition)
    print("Multiplication =", multiplication)
    print("Subtraction =", subtraction)
    print("Division =", division)

add()
